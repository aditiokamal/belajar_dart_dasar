void main() {
  var first = 10;
  var second = 3;

  print(first + second); //operator aritmatika penambahan
  print(first - second); //operator aritmatika pengurangan
  print(first * second); //operator aritmatika perkalian
  print(first / second); //operator aritmatika pembagian hasil Double
  print(first ~/ second); //operator aritmatika pembagian hasil Integer
  print(first % second); //operator aritmatika modulus/sisa bagi
}
