//praktek penggunaan operator perbandingan
void main() {
  print(10 == 10); //operator perbandingan sama dengan
  print(10 != 10); //operator perbandingan tidak sama dengan
  print(10 < 10); //operator perbandingan kurang dari
  print(10 > 10); //operator perbandingan lebih dari
  print(10 <= 10); //operator perbandingan kurang dari sama dengan
  print(10 >= 10); //operator perbandingan lebih darii sama dengan

  print('test' == 'test'); //operator perbandingan sama dengan
  print('test' != 'test'); //operator perbandingan tidak sama dengan
}
